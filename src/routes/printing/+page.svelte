    <h2 id="instructions">Printing instructions</h2>
    <p>
        The latest printable files are released under the <a rel="noopener" href="https://github.com/OWASP/cornucopia/releases/tag/pre-release">pre-release</a>. Please download final printable files from there to ensure you get the latest updates and fixes done to the decks.<br/>
        The docx/pdf files can be easily printed by any desktop printer, but for the best quality use the idml InDesign files. When sending the files to a printing facility you may have to supply the fonts that has been used in order to create the work. <br/>
        In case the printing facility doesn't have the fonts at hand you'll find the installable fonts under `resources/templates/Fonts` in this repository. They are both open source and free for commercial use.<br/>
        The fonts can also be downloaded from the web.<br/>
        Fivo Sans: <a rel="noopener nofollow" href="https://www.fontsc.com/font/fivo-sans">https://www.fontsc.com/font/fivo-sans</a><br/>
        Atkinson Hyperlegible: <a rel="noopener nofollow" href="https://brailleinstitute.org/freefont">https://brailleinstitute.org/freefont</a>
    </p>
    <p>
        The following fonts are used:
    </p>
